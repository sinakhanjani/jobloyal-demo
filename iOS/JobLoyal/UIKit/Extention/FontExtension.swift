import UIKit

extension UIFont {
    static func avenirNextBold(size: CGFloat) -> UIFont {
        UIFont(name: "Avenir Next Bold", size: size)!
    }
    
    static func avenirNextMedium(size: CGFloat) -> UIFont {
        UIFont(name: "Avenir Next Medium", size: size)!
    }
}
